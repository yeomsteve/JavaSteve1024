package day02;

public class OperatorEx02 {

	public static void main(String[] args) {
		int a = 10, b = 10;
		
		System.out.println("증가 전 : a = " + a + ", b= " + b);
		System.out.println("증가 중 : a = " + ++a + ", b= " + b++);
		System.out.println("증가 후 : a = " + a + ", b= " + b);
		
		a = 10;
		b = 10;
		System.out.println("증가 전 : a = " + a + ", b= " + b);
		++a;
		System.out.println("증가 중 : a = " + a + ", b= " + b);
		++b;
		System.out.println("증가 후 : a = " + a + ", b= " + b);
		                                                                                                                                                                                                                                                                                                                                                                                                               
		
	}

}

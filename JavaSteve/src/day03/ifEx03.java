package day03;

public class ifEx03 {

	public static void main(String[] args) {
		int num = -1;
		
		if(num>0)  {
			System.out.println(num + " is positive number");
		}
		else if (num==0) {
			System.out.println(num + " is zero");
		}
		else { 
			System.out.println(num + " is negative number");
		}

	}

}

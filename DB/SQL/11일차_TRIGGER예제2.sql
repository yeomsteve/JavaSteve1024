use cgv;
DROP TRIGGER IF EXISTS INSERT_TICKETING;

DELIMITER //
CREATE TRIGGER INSERT_TICKETING AFTER INSERT
ON TICKETING
FOR EACH ROW
BEGIN
UPDATE SCREEN_SCHEDULE
	SET SS_POSSIBLE_SEAT = SS_POSSIBLE_SEAT - NEW.TI_AMOUNT
WHERE SS_NUM = NEW.TI_SS_NUM;
END //
DELIMITER ;
